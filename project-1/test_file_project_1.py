def add(a, b):
    """Compute the sum of two numbers.
    Args:
        a (int, float): The first number.
        b (int, float): The second number.

    Returns:
        int, float: The sum of a and b.
    """
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b